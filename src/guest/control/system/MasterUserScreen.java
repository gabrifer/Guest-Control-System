    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        Dorms dorm = new Dorms();
        try {
            dorm.setName(dormName.getText());
            dorm.setAddress(address.getText());
            dorm.setRAname(RAname.getText());
            dorm.setNumberOfRooms(Integer.parseInt(numberOfRooms.getText()));
            if (maleButton.isSelected()) {
                dorm.setDormType(1);
            } else if (femaleButton.isSelected()) {
                dorm.setDormType(2);
            } else if (coedButton.isSelected()) {
                dorm.setDormType(3);
            }

            dorm.setMaxDorm(Integer.parseInt(maxDorm.getText()));
            dorm.setMaxHost(Integer.parseInt(maxHost.getText()));
            dorm.setMaxRoom(Integer.parseInt(maxRoom.getText()));

            dorm.generanteLogin();
            dorm.generatePassword();

            GuestControlSystem.dormsLst.add(dorm);

            GuestControlSystem.previousModel.addElement(dorm.getName());

            JOptionPane.showMessageDialog(null, "Dorm " + dorm.getName() + " Created\nLogin: " + dorm.getLogin() + " Password: " + dorm.getPassword());

            dormName.setText("");
            address.setText("");
            RAname.setText("");
            numberOfRooms.setText("");
            buttonGroup.clearSelection();

            maxDorm.setText("");
            maxRoom.setText("");
            maxHost.setText("");
            
            int index = GuestControlSystem.dormsLst.size()-1;
            GuestControlSystem.dB.addDorm(index, dorm.getName(), dorm.getAddress(), dorm.getRAname(), dorm.getDormType(), dorm.getMaxDorm(), dorm.getMaxRoom(), dorm.getMaxHost(), dorm.getLogin(), dorm.getPassword());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERROR: " + e + "\n Fill in all fields corectly");

        }
        
        
    }//GEN-LAST:event_addButtonActionPerformed

