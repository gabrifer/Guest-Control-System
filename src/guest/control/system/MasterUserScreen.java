    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        
        for (Dorms dormLst : GuestControlSystem.dormsLst) {
            if (dormLst.getName().equals(list.getSelectedValue())) {
                GuestControlSystem.dormsLst.remove(dormLst);
                GuestControlSystem.previousModel.remove(list.getSelectedIndex());
                break;
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

